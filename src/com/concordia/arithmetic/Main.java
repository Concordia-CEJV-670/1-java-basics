package com.concordia.arithmetic;

public class Main {

	public static void main(String[] args) {
		int result1 = 10 / 3;
		System.out.println(result1);

		double result2 = 10.0 / 3.0;
		System.out.println(result2);

		int x = 10;
		x++;
		System.out.println(x);
		System.out.println(x++);
		System.out.println(++x);

		System.out.println(10 + 3 * 2);
	}

}
